    private void dameTodoAlbum(int id) {
        List<AlbumCancion> miListaAlbumCancion = miCancionDao.dameTodoPorId(id);
        modeloTabla = (DefaultTableModel) tablaAlbumCancion.getModel();
        Object[] o = new Object[6];
        for (int i = 0; i < miListaAlbumCancion.size(); i++) {
            o[0] = miListaAlbumCancion.get(i).getCancion_id();
            o[1] = miListaAlbumCancion.get(i).getNombre_cancion();
            o[2] = miListaAlbumCancion.get(i).getDescripcion();
            o[3] = miListaAlbumCancion.get(i).getGrupo_musical();
            o[4] = miListaAlbumCancion.get(i).getNombre();
            o[5] = miListaAlbumCancion.get(i).getAnno();
            modeloTabla.addRow(o);
        }
        tablaAlbumCancion.setModel(modeloTabla);

    }

    void limpiarTabla() {
        for (int i = 0; i < modeloTabla.getRowCount(); i++) {
            modeloTabla.removeRow(i);
            i = i - 1;
        }
    }

    private void borrarCancion() {
        int fila = tablaAlbumCancion.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "No has seleccionado ninguna cancion, para borrar!!!");
        } else {
            id_seleccionado = Integer.parseInt(tablaAlbumCancion.getValueAt(fila, 0).toString());
            miCancionDao.eliminar(id_seleccionado);
            String nombre_cancion = tablaAlbumCancion.getValueAt(fila, 1).toString();
            String descripcion = tablaAlbumCancion.getValueAt(fila, 2).toString();
            String grupo_musical = tablaAlbumCancion.getValueAt(fila, 3).toString();
            String album = tablaAlbumCancion.getValueAt(fila, 4).toString();
            int anno = Integer.parseInt(tablaAlbumCancion.getValueAt(fila, 5).toString());
            limpiar();

        }

    }

    private void limpiar() {
        formnombreCancion.setText("");
        formDescripcionCancion.setText("");
    }

    private void addCancion() {
        String nombre_cancion = formnombreCancion.getText();
        String descripcion = formDescripcionCancion.getText();

        Object[] o = new Object[3];
        o[0] = nombre_cancion;
        o[1] = descripcion;
        o[2] = id;

        miCancionDao.guardar(o);
        limpiar();

    }

    private void actualizar() {
        int fila = tablaAlbumCancion.getSelectedRow();
        if (fila == -1) {
            JOptionPane.showMessageDialog(this, "para poder modificar una cancion deberias "
                    + "seleccionarla previamente!!!!");
        } else {
            String nombre_cancion = formnombreCancion.getText();
            String descripcion = formDescripcionCancion.getText();
            Object[] o = new Object[3];
            o[0] = nombre_cancion;
            o[1] = descripcion;
            o[2] = id_seleccionado;
            miCancionDao.actuaizar(o);
        }

        limpiar();

    }

